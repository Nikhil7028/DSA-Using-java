package Basics;

public class TypeCasting
{
    public static void main(String[] args)
    {
        
        System.out.println(5/2);        //    int/int =result int
        System.out.println(5.0/2.0);    //    double/double =result double
        System.out.println(5/2.0);      //    int/double =result double
        System.out.println(5.0/2);      //    double/double =result double
        
    }
    
}
